const importLine = "import { t } from '../i18n/i18n';";

for (const file of files) {
  let content = fs.readFileSync(file, 'utf8');

  // Add import if not already present
  if (!content.includes(importLine)) {
    const lastImportIdx = content.lastIndexOf('\nimport ');
    const endOfLastImport = content.indexOf('\n', lastImportIdx + 1);
    content = content.slice(0, endOfLastImport + 1) + importLine + '\n' + content.slice(endOfLastImport + 1);
    console.log(`Added import to ${file}`);
  }

  // Apply all replacements
  let count = 0;
  for (const [from, to] of MAP) {
    const regex = new RegExp(from.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g');
    const newContent = content.replace(regex, to);
    if (newContent !== content) {
      count++;
      content = newContent;
    }
  }

  fs.writeFileSync(file, content, 'utf8');
  console.log(`${file}: ${count} string types replaced`);
}
